<h3 id="项目proposal">项目Proposal</h3>

<p>项目名称：MetaMusic - 一个基于音乐的心理保健社区
</p><p>项目开始时间：2021 年 12 月
</p><p>问题：IT如何为心理亚健康人群普及认知行为疗法？
</p><p>想法：在在线心理保健社区中通过音乐日记开发和实践认知行为疗法。
</p><p>目标用户：心理亚健康人群</p>

<h4 id="介绍">介绍</h4>

<p>香港大学的一项调查显示：<a href="https://www.hkupop.hku.hk/english/report/subhealth/content/resources/pr.pdf">97%的香港市民健康状况不佳</a>[ ^1]，而中国科学院心理研究所的统计结果显示，中国70%以上的人处于心理亚健康状态。越来越多的人开始意识到这个警告信号，并前往医院就诊。然而，特别是在大陆，医院相关资源短缺，往往需要4到6个月的时间才能预约。这个项目——MetaMusic，旨在通过IT与音乐日记、<a href="https://betterhumans.coach.me/cognitive-journaling-a-systematic-method-to-overcome-negative-beliefs-119be459842c">认知行为疗法</a><sup id="fnref:2" role="doc-noteref"><a href="#fn:2" class="footnote" rel="footnote">1</a></sup> 和可分享的<a href="https://my.clevelandclinic.org/health/treatments/8817-music-therapy">即兴音乐疗法</a><sup id="fnref:3" role="doc-noteref"><a href="#fn:3" class="footnote" rel="footnote">2</a></sup>等技术整合，帮助更多受到心理亚健康困扰的人。</p>

<h5 id="心理保健的心理治疗">心理保健的心理治疗</h5>

<p>认知行为疗法 (CBT) 是一种心理治疗形式，已被证明与其他形式的心理疗法或精神科药物一样有效，用于治疗包括抑郁症、焦虑症、婚姻问题、饮食障碍和严重的精神疾病等一系列问题。 CBT 强调帮助个人学习成为自己的治疗师。大量研究表明，CBT 可显着改善心理健康和生活质量。<sup id="fnref:6" role="doc-noteref"><a href="#fn:6" class="footnote" rel="footnote">3</a></sup> 然而，CBT 需要一些基本的心理技能，除非你特别擅长自我调节，否则很难坚持下去。</p>

<h5 id="用-it-将音乐和-cbt-集成到心理疗愈中">用 IT 将音乐和 CBT 集成到心理疗愈中</h5>

<p>人们总是因为忽略了他们情感背后的信念而难以控制情绪，然而，听音乐能够很好的舒缓人们的情绪。音乐是人类与生俱来的能力，它可以帮助大多数人获得更好的心理健康。此外，通过音乐日记开发和练习情绪调节可以整合认知行为疗法——帮助人们挑战和建立更好的信念。随着技术的发展，算法作曲和/或音乐游戏已经成为一种最容易学习和使用的新工具——有一些开源框架，包括<a href="https://apps.musedlab.org /groovepizza/?museid=kngevZcmc&amp;">Tone.js</a><sup id="fnref:4" role="doc-noteref"><a href="#fn:4" class="footnote" rel="footnote">4</a></sup> 和 <a href="https://experiments.withgoogle.com/ai/ai-duet/view/">洋红色</a><sup id="fnref:5" role="doc-noteref"><a href="#fn:5" class="footnote" rel="footnote">5</a></sup>。该项目将应用这些开源框架和游戏化（通过音乐）CBT 在智能手机上提供心理保健。最后，该项目将开源，所有的小组操作、会议记录、活动和计划都在<a href="https://meta-music.github.io/project/">meta-music.github.io</a>上展示。</p>

<h3 id="project-proposal">Project Proposal</h3>

<p>Project title:  MetaMusic - A Music-based Mental Healthcare Community</p>

<p>Project start: December 2021</p>

<p>Problem:  How IT can popularize cognitive behavioral therapy for mental sub-health people?</p>

<p>Idea:  Develop and practice emotional regulation by music diary in an online Mental Healthcare Community.</p>

<p>Intended impact: Mental sub-health people</p>

<h4 id="introduction">Introduction</h4>

<p>The university of Hong Kong made a survey revealed:  <a href="https://www.hkupop.hku.hk/english/report/subhealth/content/resources/pr.pdf">97% Hong Kong citizens have sub-optimal health</a><sup id="fnref:1" role="doc-noteref"><a href="#fn:1" class="footnote" rel="footnote">6</a></sup>, and statistical results of the Psychological Institute of the Chinese Academy of Sciences show that more than 70% of China is in mental sub-health. More and more people began to realize this warning signs, and go to the hospital. However, Especially in the mainland, there is a shortage of hospital-related resources, which often takes 4 to 6 months to make an appointment. Therefore, this project - MetaMusic, aims to help more people who are troubled by mental sub-health through IT and some other technology integration such as music diary, computerized <a href="https://betterhumans.coach.me/cognitive-journaling-a-systematic-method-to-overcome-negative-beliefs-119be459842c">cognitive behavioral therapy</a><sup id="fnref:2:1" role="doc-noteref"><a href="#fn:2" class="footnote" rel="footnote">1</a></sup> and shareable <a href="https://my.clevelandclinic.org/health/treatments/8817-music-therapy">improvisational music therapy</a><sup id="fnref:3:1" role="doc-noteref"><a href="#fn:3" class="footnote" rel="footnote">2</a></sup>.</p>

<h5 id="psychological-treatment-for-mental-healthcare">Psychological Treatment for Mental Healthcare</h5>

<p>Cognitive behavioral therapy (CBT) is a form of psychological treatment that has been demonstrated to be as effective as, or more effective than, other forms of psychological therapy or psychiatric medications for a range of problems including depression, anxiety disorders, marital problems, eating disorders, and severe mental illness. CBT places an emphasis on helping individuals learn to be their own therapists. Numerous research studies suggest that CBT leads to significant improvement in functioning and quality of life.<sup id="fnref:6:1" role="doc-noteref"><a href="#fn:6" class="footnote" rel="footnote">3</a></sup> However, CBT require some basic psychological skills and difficult to persist unless you are particularly good at self-regulation.</p>

<h5 id="integrating-music-diary-and-cbt-into-mental-healthcare-using-it">Integrating Music Diary and CBT into Mental Healthcare using IT</h5>

<p>People always not really get the meaning of their emotion, but can feel better when they listen to music. Music is the innate ability of human beings which can help most people to get better mental health. Moreover, developing and practicing emotional regulation by music diaries can integrate cognitive behavioral therapy - help people to challenge and build better beliefs. With the development of technology, algorithmic composition and/or music games has become a new instrument that is most easy to learn and use - there are some open-source framework including <a href="https://apps.musedlab.org/groovepizza/?museid=kngevZcmc&amp;">Tone.js</a><sup id="fnref:4:1" role="doc-noteref"><a href="#fn:4" class="footnote" rel="footnote">4</a></sup> and <a href="https://experiments.withgoogle.com/ai/ai-duet/view/">magenta</a><sup id="fnref:5:1" role="doc-noteref"><a href="#fn:5" class="footnote" rel="footnote">5</a></sup>. This project will apply those open-source framework, and gamifying (by music) CBT to deliver mental healthcare on smartphones. Finally, the project will be open source, and all group operations, meeting records, activities, and plans are showing on <a href="https://meta-music.github.io/project/">meta-music.github.io</a>.</p>

